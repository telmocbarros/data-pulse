func TestComputeHistogramFromDatasetSingleValueShortCircuit(t *testing.T) {
	// All rows have value 7. The single-distinct-value fast path
	// produces one bucket spanning [7, 7] with the full count.
	setupIntegration(t)
	datasetId, tableName := makeFixture(t, []fixtureColumn{
		{"v", columntype.Numerical},
	})
	insertRows(t, tableName, datasetId, []string{"v"}, [][]any{
		{7.0}, {7.0}, {7.0}, {7.0},
	})

	got, err := ComputeHistogramFromDataset(tableName, []string{"v"}, 5)
	if err != nil {
		t.Fatalf("ComputeHistogramFromDataset: %v", err)
	}
	buckets, ok := got["v"]
	if !ok {
		t.Fatal("missing v from histogram result")
	}
	if len(buckets) != 1 {
		t.Fatalf("got %d buckets, want 1 (single-distinct-value fast path)", len(buckets))
	}
	if !almostEqual(buckets[0].Min, 7) || !almostEqual(buckets[0].Max, 7) {
		t.Errorf("bucket bounds = (%v, %v), want (7, 7)", buckets[0].Min, buckets[0].Max)
	}
	if buckets[0].Count != 4 {
		t.Errorf("bucket count = %d, want 4", buckets[0].Count)
	}
}

