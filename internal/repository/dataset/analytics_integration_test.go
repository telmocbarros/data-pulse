func TestGetCategoryBreakdownGroupedTopValuesAndOrdering(t *testing.T) {
	// `region` is the top-level column, `tier` the groupBy.
	// Counts per region: us=4, eu=3, asia=2.
	// limit=2 picks {us, eu} as the top values.
	// Within us: gold=2, silver=2 → tied count, ordered by group asc.
	// Within eu: gold=2, silver=1.
	setupIntegration(t)
	datasetId, tableName := makeFixture(t, []fixtureColumn{
		{"region", columntype.Categorical},
		{"tier", columntype.Categorical},
	})
	insertRows(t, tableName, datasetId, []string{"region", "tier"}, [][]any{
		{"us", "gold"}, {"us", "gold"},
		{"us", "silver"}, {"us", "silver"},
		{"eu", "gold"}, {"eu", "gold"},
		{"eu", "silver"},
		{"asia", "gold"}, {"asia", "silver"},
	})

	got, err := GetCategoryBreakdownGrouped(tableName, "region", "tier", 2)
	if err != nil {
		t.Fatalf("GetCategoryBreakdownGrouped: %v", err)
	}
	// Expected (sorted by total desc, then v asc; within v by count desc, then group asc):
	//   us/gold=2, us/silver=2  (tied -> alphabetical by group)
	//   eu/gold=2, eu/silver=1
	want := []CategoryBreakdownGroupedCell{
		{Value: "us", Group: "gold", Count: 2},
		{Value: "us", Group: "silver", Count: 2},
		{Value: "eu", Group: "gold", Count: 2},
		{Value: "eu", Group: "silver", Count: 1},
	}
	if len(got) != len(want) {
		t.Fatalf("got %d rows, want %d", len(got), len(want))
	}
	for i, w := range want {
		if got[i].Value != w.Value || got[i].Group != w.Group || got[i].Count != w.Count {
			t.Errorf("row %d = %+v, want %+v", i, got[i], w)
		}
	}
}

