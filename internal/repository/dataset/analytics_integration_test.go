func TestComputeHistogramFromDatasetRange(t *testing.T) {
	// Values 0..9 with bins=5 → bucket width 2, each bucket has 2 values
	// and the max value (9) is clamped into bucket 5 by LEAST(WIDTH_BUCKET..., 5).
	setupIntegration(t)
	datasetId, tableName := makeFixture(t, []fixtureColumn{
		{"v", columntype.Numerical},
	})
	rows := make([][]any, 10)
	for i := range rows {
		rows[i] = []any{float64(i)}
	}
	insertRows(t, tableName, datasetId, []string{"v"}, rows)

	got, err := ComputeHistogramFromDataset(tableName, []string{"v"}, 5)
	if err != nil {
		t.Fatalf("ComputeHistogramFromDataset: %v", err)
	}
	buckets := got["v"]
	if len(buckets) != 5 {
		t.Fatalf("got %d buckets, want 5", len(buckets))
	}
	var total int64
	for i, b := range buckets {
		// Bucket i covers [i*1.8, (i+1)*1.8) in width units of (9-0)/5 = 1.8.
		wantMin := float64(i) * 1.8
		wantMax := float64(i+1) * 1.8
		if !almostEqual(b.Min, wantMin) || !almostEqual(b.Max, wantMax) {
			t.Errorf("bucket %d bounds = (%v, %v), want (%v, %v)", i, b.Min, b.Max, wantMin, wantMax)
		}
		total += b.Count
	}
	if total != 10 {
		t.Errorf("histogram total = %d, want 10 (max-value clamping into top bucket)", total)
	}
}

