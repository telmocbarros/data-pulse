// Integration tests for analytics.go, gated behind the `integration`
// build tag (same convention as the pipeline integration test). Each
// test creates a fresh fixture dataset (real per-dataset table + real
// rows inserted via direct SQL), runs an analytics query, asserts on
// the result, and cleans up.
//
// Run as:
//
//	go test -tags=integration -v ./internal/repository/dataset/...
//
// Prerequisites (same as the pipeline integration test):
//   - `docker compose up -d` (Postgres on localhost:5432)
//   - `go run ./cmd/migrate` (applies internal/repository/migrations/*.sql)

