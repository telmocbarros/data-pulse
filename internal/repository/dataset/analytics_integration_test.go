// setupIntegration ensures a connection to the dev DB. Skips on failure
// so CI without the dev DB is unaffected.
func setupIntegration(t *testing.T) {
	t.Helper()
	if config.Storage != nil {
		return
	}
	if err := config.SetupDatabase(); err != nil {
		t.Skipf("integration test skipped: %v (run `docker compose up -d && go run ./cmd/migrate` first)", err)
	}
}

// fixtureColumn names a column and its logical type tag.
type fixtureColumn struct {
	name string
	kind string // one of columntype.Numerical/Boolean/Date/Categorical
}

// makeFixture creates a fresh dataset (datasets row + per-dataset table
// + dataset_columns rows), registers a t.Cleanup, and returns the
// dataset id and table name. Callers INSERT rows themselves so each
// test controls its data shape exactly.
func makeFixture(t *testing.T, cols []fixtureColumn) (datasetId, tableName string) {
	t.Helper()
	pairs := make([][]string, len(cols))
	for i, c := range cols {
		pairs[i] = []string{c.name, c.kind}
	}
	tableName, err := CreateDatasetTable("test", pairs)
	if err != nil {
		t.Fatalf("CreateDatasetTable: %v", err)
	}
	datasetId, err = StoreDatasetMetadata(map[string]any{
		"name":        t.Name() + ".csv",
		"tableName":   tableName,
		"size":        int64(0),
		"author":      "integration-test",
		"description": t.Name(),
	})
	if err != nil {
		_, _ = config.Storage.Exec(fmt.Sprintf("DROP TABLE IF EXISTS %s", tableName))
		t.Fatalf("StoreDatasetMetadata: %v", err)
	}
	if err := StoreDatasetColumns(pairs, datasetId); err != nil {
		_, _ = config.Storage.Exec(fmt.Sprintf("DROP TABLE IF EXISTS %s", tableName))
		_, _ = config.Storage.Exec(`DELETE FROM datasets WHERE id=$1`, datasetId)
		t.Fatalf("StoreDatasetColumns: %v", err)
	}
	t.Cleanup(func() {
		if !sqlsafe.IsValidIdentifier(tableName) {
			t.Errorf("cleanup: invalid table name %q", tableName)
			return
		}
		if _, err := config.Storage.Exec(fmt.Sprintf("DROP TABLE IF EXISTS %s", tableName)); err != nil {
			t.Logf("cleanup: dropping %s: %v", tableName, err)
		}
		if _, err := config.Storage.Exec(`DELETE FROM datasets WHERE id=$1`, datasetId); err != nil {
			t.Logf("cleanup: deleting dataset %s: %v", datasetId, err)
		}
	})
	return datasetId, tableName
}

// insertRows inserts len(rows) rows into the fixture table. Each row in
// rows must have len(colNames) values in column order. dataset_id is
// auto-filled from datasetId; id is auto-generated by the table default.
func insertRows(t *testing.T, tableName, datasetId string, colNames []string, rows [][]any) {
	t.Helper()
	if !sqlsafe.IsValidIdentifier(tableName) {
		t.Fatalf("insertRows: invalid table name %q", tableName)
	}
	for _, c := range colNames {
		if !sqlsafe.IsValidIdentifier(c) {
			t.Fatalf("insertRows: invalid column %q", c)
		}
	}
	for i, row := range rows {
		args := append([]any{datasetId}, row...)
		ph := make([]string, len(args))
		for j := range ph {
			ph[j] = fmt.Sprintf("$%d", j+1)
		}
		q := fmt.Sprintf(
			"INSERT INTO %s (dataset_id, %s) VALUES (%s)",
			tableName, strings.Join(colNames, ", "), strings.Join(ph, ", "),
		)
		if _, err := config.Storage.Exec(q, args...); err != nil {
			t.Fatalf("insertRows row %d: %v", i, err)
		}
	}
}

// almostEqual is a small float-comparison helper for histogram bucket
// bounds (where division-by-N produces non-exact values).
func almostEqual(a, b float64) bool {
	return math.Abs(a-b) < 1e-9
}

