func TestGetScatterPlotFromDataset(t *testing.T) {
	setupIntegration(t)
	datasetId, tableName := makeFixture(t, []fixtureColumn{
		{"x", columntype.Numerical},
		{"y", columntype.Numerical},
	})
	insertRows(t, tableName, datasetId, []string{"x", "y"}, [][]any{
		{1.0, 10.0},
		{2.0, 20.0},
		{nil, 30.0}, // x null — must be filtered
		{4.0, nil},  // y null — must be filtered
		{5.0, 50.0},
	})

	got, err := GetScatterPlotFromDataset(tableName, "x", "y", 100)
	if err != nil {
		t.Fatalf("GetScatterPlotFromDataset: %v", err)
	}
	if len(got) != 3 {
		t.Fatalf("got %d points, want 3 (the two null-containing rows must be filtered)", len(got))
	}
	// Order is undefined (no ORDER BY in the query); check by collecting.
	wantPairs := map[float64]float64{1.0: 10.0, 2.0: 20.0, 5.0: 50.0}
	for _, p := range got {
		want, ok := wantPairs[p.X]
		if !ok {
			t.Errorf("unexpected point x=%v", p.X)
			continue
		}
		if p.Y != want {
			t.Errorf("for x=%v: y=%v, want %v", p.X, p.Y, want)
		}
	}
}

